# create a variable of type string
str = 'Nandhakumar'
# prints complete string
print(str)  # Nandhakumar

# prints first character of the string
print(str[0])  # P

# prints characters starting from 2nd to 5th
print(str[2:7])  # nat

# length of string
print(len(str))  # 8

# concatenate string
print(str + "TEST")  # Nandhakumar