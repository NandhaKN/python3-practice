#variables declaration

Var = 10
print(Var)  # 10
# print its type
print(type(Var))  # <class 'int'>

# assign different integer value to var
var = 55
print(var)  # 55

# change var to string
var = "Now I'm a Developer"
print(var)  #Now I'm a Developer
# print its type
print(type(var))  # <class 'str'>

# change var to float
var = 35.69
print(var)  # 35.69
# print its type
print(type(var))  # <class 'float'>